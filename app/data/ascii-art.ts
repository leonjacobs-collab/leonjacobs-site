export const GRID_WIDTH = 130;
export const GRID_HEIGHT = 45;

function pad(lines: string[]): string[] {
  return Array.from({ length: GRID_HEIGHT }, (_, i) =>
    (lines[i] || '').padEnd(GRID_WIDTH).slice(0, GRID_WIDTH)
  );
}

